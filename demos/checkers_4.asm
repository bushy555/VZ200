	LD 	hl, 0x6800
b21:	BIT 	7,  (hl)
	jr	z,  b21

	ld	hl, screen +2048-32		; GRAB last line.
	ld	de, screen 
	ld	bc, 32
	ldir
	ld	hl, screen+2048-32 	
	ld	de, screen+2048
	ld	bc, 2040
	lddr

