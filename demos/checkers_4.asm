	LD 	hl, 0x6800
b11:	BIT 	7,  (hl)
	jr	z, b11

	ld	hl, screen 			; GRAB last line.
	ld	de, screen + 2048 
	ld	bc, 32
	ldir

	ld	hl, screen + 32	
	ld	de, screen 
	ld	bc, 2040
	ldir

