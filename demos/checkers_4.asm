; SHRG, 256x192 (AUS).  MOVING CHECKERS DEMO.
; Bank 0 : moving down.
; Bank 1 : moving up
; Bank 2 : moving up
;                               SPLITTING SCREEN AT BOTTOM OF BANK 0. AT Y ROW : 64
;
; Requires New incoming line 000 coming in from the top for Bank 0
;          New incoming line 192 coming in from the top for Bank 2
;          Top of bank 2 (top of bottom) to be consistent into bottom of Bank 1 (bottom of middle)
;
;
;0     128       256  1-bit = 1 pixel
;
;
;64               |
;
;96               |
;
;128              |
;
;                 |
;
;192   -----     256
;
;
;
	org	$8000

	ld	a, $8;FF		; SHRG MODE(1) for copy of latch.  $EF = COLOUR,0    $FF = COLOUR,1.
	ld	(30779), a
	ld	a, $8;FF		; SHRG MODE(1) for Latch.	   $EF = COLOUR,0    $FF = COLOUR,1.
	ld	($6800), a


screen		equ	$7000
buffer		equ	$C000
buffer2		equ	$B800
leftover_wrap	equ	32




start:	di
	ld	hl, pic		
	ld	a, 28		; BANK 0. First 2k of screen.
	out	(32), a
	ld	de, $7000
 	ld	bc, 2048		
	ldir


	ld	a, 29		; BANK 0. First 2k of screen.
	out	(32), a
	ld	de, $7000
 	ld	bc, 2048		
	ldir

	ld	a, 30		; BANK 0. First 2k of screen.
	out	(32), a
 	ld	bc, 2048
	ld	de, $7000
	ldir

; ================================================================
; COPY to buffer


	ld	hl, $7000		
	ld	a, 28		; BANK 0. First 2k of screen.
	out	(32), a
	ld	de, buffer
 	ld	bc, 2048		
	ldir


	ld	a, 29		; BANK 0. First 2k of screen.
	out	(32), a
	ld	hl, $7000		
 	ld	bc, 2048		
	ldir

	ld	a, 30		; BANK 0. First 2k of screen.
	out	(32), a
	ld	hl, $7000		
 	ld	bc, 2048
	ldir



loop2:	ld 	a, ($68ef)		; Key : SPACE
	and	$10	
	jr 	nz, loop2		; Loop forever until <SPACE> is pressed.


