 ;SCREEN 0 - SPLAT.
SC0:
 	push	hl
	push	de
	push	af
	push	bc
 	LD 	HL,SBF1
 	LD 	DE,SSCN
 	LD 	BC,SZSC
 	LDIR
 	CALL 	DPLY
	pop	bc
	pop	af
	pop	de
	pop	hl
 	RET







 ;SCREEN 1 - OPEN-UP.
SC1 	push	hl
	push	de
	push	af
	push	bc

 	PUSH 	IX
 	PUSH 	IY
 	LD 	HL,SBF1+HSZC
 	LD 	DE,SSCN+HSZC
 	PUSH 	HL
 	POP 	IX
 	PUSH 	DE
 	POP 	IY
 	DEC 	HL
 	DEC 	DE
 	LD 	B,20H
NLN1 	PUSH 	BC
 	LD 	BC,LLEN
 	LDDR
 	PUSH 	HL
 	PUSH 	DE
 	PUSH 	IX
 	POP 	HL
 	PUSH 	IY
 	POP 	DE
 	LD 	BC,LLEN
 	LDIR
 	PUSH 	HL
 	POP 	IX
 	PUSH 	DE
 	POP 	IY
 	CALL 	DPLY
 	POP 	DE
 	POP 	HL
 	POP 	BC
 	DJNZ 	NLN1
 	POP 	IY
 	POP 	IX

	pop	bc
	pop	af
	pop	de
	pop	hl

 	RET


 ;SCREEN 2 - ROLL DOWN.
SC2 	
 	push	hl
	push	de
	push	af
	push	bc

 	LD 	HL,SBF2
 	LD 	DE,SSCN
 	LD 	B,NLIN
NLN2 	PUSH 	BC
 	LD 	BC,LLEN
 	LDIR
 	CALL 	DPLY
 	POP 	BC
 	DJNZ 	NLN2
	pop	bc
	pop	af
	pop	de
	pop	hl

 	RET


 ;SCREEN 3 PUSH DOWN.
SC3 	 	
	push	hl
	push	de
	push	af
	push	bc

	LD 	HL,SBF3-1
 	LD 	B,NLIN+1
NSN3 	PUSH 	BC
 	LD 	DE,ESCN-1
 	LD 	BC,SZSC
 	LDDR
 	LD 	BC,SZSC-LLEN
 	ADD 	HL,BC
 	CALL 	DPLY
 	POP 	BC
 	DJNZ 	NSN3
	pop	bc
	pop	af
	pop	de
	pop	hl

 	RET


 ;SCREEN 4 - ROLL UP.
SC4 	
 	push	hl
	push	de
	push	af
	push	bc
 	LD 	HL,SBF4-1
 	LD 	DE,ESCN-1
 	LD 	B,NLIN
NLN4 	PUSH 	BC
 	LD 	BC,LLEN
 	LDDR
 	CALL 	DPLY
 	POP 	BC
 	DJNZ 	NLN4
 	pop	bc
	pop	af
	pop	de
	pop	hl

 	RET



 ;SCREEN 5 - PUSH UP.
SC5 	push	hl
	push	de
	push	af
	push	bc
	LD 	HL,SBF5-1
 	LD 	B,NLIN+1
NSN5 	PUSH 	BC
 	LD 	DE,ESCN-1
 	LD 	BC,SZSC
 	LDDR
 	LD 	BC,SZSC+LLEN
 	ADD 	HL,BC
 	CALL 	DPLY
 	POP 	BC
 	DJNZ 	NSN5
	pop	bc
	pop	af
	pop	de
	pop	hl

 	RET



 ;SCREEN 6 - 4 BAR ROLL DOWN.
SC6 	
 	push	hl
	push	de
	push	af
	push	bc
 	LD 	HL,SBF6
 	LD 	DE,SSCN
 	LD 	B,10H
 	NBR6 	PUSH BC
 	LD 	B,4H
NLN6 	PUSH 	BC
 	LD 	BC,LLEN
 	LDIR
 	LD 	BC,200H-LLEN
 	ADD 	HL,BC
 	EX 	DE,HL
 	ADD 	HL,BC
 	EX 	DE,HL
 	POP 	BC
 	DJNZ 	NLN6
 	LD 	BC,SZSC-LLEN
 	OR 	A
 	SBC 	HL,BC
 	EX 	DE,HL
 	OR 	A
 	SBC 	HL,BC
 	EX 	DE,HL
 	CALL 	DPLY
 	POP 	BC
 	DJNZ 	NBR6
	pop	bc
	pop	af
	pop	de
	pop	hl

 	RET


 ;SCREEN 7 - L TO R SWEEP.
SC7 	
 	push	hl
	push	de
	push	af
	push	bc

 	PUSH 	IX
 	PUSH 	IY
 	LD 	IX,SBF7
 	LD 	IY,SSCN
 	LD 	B, LLEN
NCL7 	PUSH 	BC
 	LD 	H,0FFH
 	LD 	B, 4
NPX7 	PUSH 	BC
 	SRL 	H
 	SRL 	H
 	LD 	A,H
 	CPL
 	LD 	L,A
 	LD 	B,NLIN
NLN7 	LD 	A,(IX+ZERO)
 	AND 	L
 	LD 	D,A
 	LD 	A,(IY+ZERO)
 	AND 	H
 	OR 	D
 	LD 	(IY+ZERO),A
 	LD 	DE,LLEN
 	ADD 	IX,DE
 	ADD 	IY,DE
 	DJNZ 	NLN7
 	CALL 	DPLY
 	LD 	DE,BIG-SZSC
 	ADD 	IX,DE
 	ADD 	IY,DE
 	POP 	BC
 	DJNZ 	NPX7
 	INC 	IX
 	INC 	IY
 	POP 	BC
 	DJNZ 	NCL7
 	POP IY
 	POP IX
	pop	bc
	pop	af
	pop	de
	pop	hl

 	RET


 ;SCREEN 8 - R TO L SWEEP.
SC8 	
 	push	hl
	push	de
	push	af
	push	bc
 	PUSH 	IX
 	PUSH 	IY
 	LD 	IX,SBF8+1FH
 	LD 	IY,SSCN+1FH
 	LD 	B,LLEN
NCL8 	PUSH 	BC
 	LD 	H,0FFH
 	LD 	B,4
NPX8 	PUSH 	BC
 	SLA 	H
 	SLA 	H
 	LD 	A,H
 	CPL
 	LD 	L,A
 	LD 	B,NLIN
NLN8 	LD 	A, (IY+ZERO)
 	AND 	L
 	LD 	D,A
 	LD 	A,(IY+ZERO)
 	AND 	H
 	OR 	D
 	LD 	(IY+ZERO),A
 	LD 	DE,LLEN
 	ADD 	IX,DE
 	ADD 	IY,DE
 	DJNZ 	NLN8
 	CALL 	DPLY
 	LD 	DE,BIG-SZSC
 	ADD 	IX,DE
 	ADD 	IY,DE
 	POP 	BC
 	DJNZ 	NPX8
 	DEC 	IX
 	DEC 	IY
 	POP 	BC
 	DJNZ 	NCL8
 	POP 	IY
 	POP 	IX
	pop	bc
	pop	af
	pop	de
	pop	hl

 	RET



 ;MOVE SCREEN BUFFER TO VRAM.
MTOP 	LD 	HL,SSCN
 	LD 	DE,VRAM
 	LD	BC,HSZC
 	LDIR
 	DI
 	LD 	BC,MBOT
 	LD 	(IVEC+1),BC
 	EI
 	RET
MBOT	LD 	HL,SSCN+HSZC
 	LD 	DE,VRAM+HSZC
 	LD 	BC,HSZC
 	LDIR				 ;MOVE BOTTOM HALF SCRN. BUF.
 	DI
 	LD 	A,TURN
 	LD 	(IVEC),A			;SET INTERRUPT VECTOR TO RET AS BOTH HALVES ARE MOVED.
 	EI
 	RET



 ;INTERRUPT DRIVEN DISPLAY ROUTINE. A PAUSE IS DONE
 ;SO THAT THE TOP AND BOTTOM HALVES OF THE SCREEN CAN BE
 ;MOVED FROM THE SCREEN BUFFER TO VRAM.
 ;NB. BC REG. IS CHANGED BY THIS SUBROUTINE.
DPLY 	LD 	A, JMP
 	LD 	BC,MTOP
 	LD 	(IVEC+1),BC
 	LD 	(IVEC),A
 	EI
 	LD 	BC,DURD				 ;DELAY MSEC.
 	CALL 	DLAY
 	DI
 	RET				 ;RETURN TO MOVE ROUTINES.
 


