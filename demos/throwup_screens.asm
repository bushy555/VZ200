 ;SCREEN 5 - PUSH UP.
 ;SCREEN 6 - 4 BAR ROLL DOWN.
 ;SCREEN 7 - L TO R SWEEP.
 ;SCREEN 8 - R TO L SWEEP.




 VRAM EQU 7000H		;START OF VIDEO SCREEN.
 SSCN EQU 0B200H	;START OF SCREEN BUFFER.
 SZSC EQU 0800H		;SIZE OF SCREEN.
 HSZC EQU 0400H		;HALF SIZE SCREEN.
 ESCN EQU SSCN+SZSC	;END OF SCREEN BUFFER+1.
 LLEN EQU 20H		;LINE LENGTH IN BYTES. 	(32 decimal)
 NLIN EQU 40H		;NUMBER OF LINES.	(64 decimal)
 BIG EQU 0FFFFH+1	;EQ. TO 65536D.
 ZERO EQU 00H		;ZERO FOR OFFSETS.
 SBF1 EQU 0C000H	;START OF PICTURE BUFFERS.
 SBF2 EQU SBF1+SZSC
 SBF3 EQU SBF2+SZSC
 SBF4 EQU SBF3+SZSC
 SBF5 EQU SBF4+SZSC
 SBF6 EQU SBF5+SZSC
 SBF7 EQU SBF6+SZSC
 SBF8 EQU SBF7+SZSC
 DLAY EQU 4038H		;DELAY ROUTINE IN DOS.
 DURD EQU 12H		;DELAY DURATION IN MSEC.
 TURN EQU 0C9H		;OPCODE FOR RETURN.
 JMP EQU 0C3H		;OPCODE FOR JUMP.
 IVEC EQU 787DH		;3 BYTE INTERRUPT VECTOR.


	ld	a, 8
	ld	($6800), a


	ld	HL, astro	; Load Astro into Buffer #1 (at $C000)
