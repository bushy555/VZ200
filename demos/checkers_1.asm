	ORG $8000


	scroll_width	equ 200
	delay1		equ 700			;short delay
	delay2		equ $FFFF		;long delay

	ld	a, 8		; SHRG MODE(1)
	ld	(30779), a
	ld	a, 8		; MODE(1)
	ld	(26624), a

start:	ld	a, 0
	ld	(x), a
	ld	a, 0
	ld	(y), a
	ld	a, 0
intro:	ld	hl, picture
	ld	de, $7000
	call	display
	call 	delay_long

start1:	ld	b, 32				;left	1. 

;-----------------------
; Scroll 1 - Left
;-----------------------
l1:	ld	a, (x)
	inc	a
	ld	(x), a
	ld	hl, picture
	ld	de, (x)
	add	hl, de
	call	display
	call 	delay_short
	djnz	l1	

;-----------------------
; Scroll 2 - Right
;-----------------------
	ld	b, 32				;right	1
l2:	ld	a, (x)
	dec	a
	ld	(x), a
	ld	hl, picture
	ld	de, (x)
	add	hl, de
	call	display
	call 	delay_short
	djnz	l2

;-----------------------
; Scroll 3 - Down
;-----------------------
	ld	b, 32				;down	2
l3:	push	bc	
	ld	a, (y)
	inc	a
	ld	(y), a
	ld	bc, 0
	ld	b, 128+64+32+32; 128;64; 96
l32:	inc	de
	djnz	l32
	ld	hl, picture
	add	hl, de
	call	display
	call 	delay_short
	pop	bc
	djnz	l3

;-----------------------
; Scroll 4 - Down and right
;-----------------------
	ld	b, 32;64;32				;down and right 4
ld1:	push	bc	
	ld	a, (y)
	inc	a
	ld	(y), a
	ld	bc, 0
	ld	b, 128; 256; 96
ld2:	inc	de
	djnz	ld2
	inc	de		; scroll right whilst down
	ld	a, (x)
	inc	a
	ld	(x), a
	ld	hl, picture
	add	hl, de
	call	display
	call 	delay_short
	pop	bc
	djnz	ld1

;-----------------------
; Scroll 5 - UP
;-----------------------
	ld	b, 64				;up	2
	call	up

;-----------------------
; Scroll 6 - Right
;-----------------------
	call	right

;-----------------------
; Scroll 7 - Left
;-----------------------
	call	left

;-----------------------
; Scroll 8 - Down
;-----------------------
	ld	b, 64;126				;down	8
	call	down
					; bottom right hand corner
;-----------------------
; Scroll 9 - Right
;-----------------------
	call	right

;-----------------------
; Scroll 10 - Up and Right
;-----------------------
	ld	b, 32				;up	10
lup3:	push	bc	
	ld	a, (y)				; up and scroll to right
	dec	a
	ld	(y), a
	ld	bc, 0
	ld	b, 128
lup31:	dec	de
	djnz	lup31
	inc	de
	ld	hl, picture
	add	hl, de
	ld	a, (x)
	inc	a
	ld	(x), a
	call	display
	pop	bc
	djnz	lup3

;-----------------------
; Scroll 11 - UP
;-----------------------
	ld	b, 32				;up	11
	call	up

;-----------------------
; Scroll 12 - Down and Left
;-----------------------
	ld	b, 16				;down	12
	call	downlscroll
					
;-----------------------
; Scroll 13 - Down
;-----------------------
	ld	b, 32				;down	13
	call	down

;-----------------------
; Scroll 14 - Down and Left
;-----------------------
	ld	b, 16				;down	14
	call	downlscroll

;-----------------------
; Scroll 15 - Left
;-----------------------
	call	left

;-----------------------
; Scroll 16 - UP
;-----------------------
	ld	b, 32;126				;up	16
	call	up

;-----------------------
; Scroll 17 - Down
;-----------------------
	ld	b, 32;64;126				;down	17
	call	down

;-----------------------
; Scroll 18 - Righticus
;-----------------------
	call	right

;-----------------------
; Scroll 19 - upicus
;-----------------------
	ld	b, 64;126				;up	19
	call	up

;-----------------------
; Scroll 20 - Lefticus
;-----------------------
	call	left

;-----------------------
; Scroll 21 - Down
;-----------------------
	ld	b, 32				;down	17
	call	downlscroll

	jp	start

; -------------------

downlscroll:
d8:	push	bc				; Scroll
	ld	a, (y)
	inc	a
	ld	(y), a
	ld	bc, 0
	ld	b, 128;96
d81:	inc	de
	djnz	d81
	dec	de
	ld	a, (x)
	dec	a
	ld	(x), a
	ld	hl, picture
	add	hl, de
	call	display
	pop	bc
	djnz	d8

	ret


down:	push	bc				
	ld	a, (y)
	inc	a
	ld	(y), a
	ld	bc, 0
	ld	b, 128; 96
d71:	inc	de
	djnz	d71
	ld	a, (x)
	dec	a
	ld	(x), a
	ld	hl, picture
	add	hl, de
	call	display
	pop	bc
	djnz	down
	ret


up:	push	bc	
	ld	a, (y)
	dec	a
	ld	(y), a
	ld	bc, 0
	ld	b, 128; 96
lup2:	dec	de
	djnz	lup2
	ld	hl, picture
	add	hl, de
	call	display
	pop	bc
	djnz	up
	ret


left:	ld	b, 32				;left	20
le4:	ld	a, (x)
	inc	a
	ld	(x), a
	inc	de
	ld	hl, picture
	add	hl, de
	call	display
	djnz	le4	
	ret

right:	ld	b, 32				;right	18
ri3:	ld	a, (x)				;FAST
	dec	a
	ld	(x), a
	dec	de
	ld	hl, picture
	add	hl, de
	call	display
	djnz	ri3	
	ret



display:
	push	de
	push	bc
	push	hl
	ld	a, 28		; BANK 0. First 2k of screen.
	ld	c, 32
	out	(c), a

	ld	de, 0
	ld	bc, 0
	ld	b, 0
	ld	a,  64		; height of 256x192 2k bank 0 screen
	ld	de, $7000
disp1: 	ld	c, 32		; 32x8=256. width of 256x192 screen
	ldir
	ld	bc, 32;64;96		; add leftover amount for wrap around.
	add	hl, bc
	dec	a
	jr	nz, disp1
	call 	delay_short





	ld	a, 29		; BANK 0. First 2k of screen.
	ld	c, 32
	out	(c), a

	ld	de, 0
	ld	bc, 0
	ld	b, 0
	ld	a,  64		; height of 256x192 2k bank 1 screen
	ld	de, $7000
disp2: 	ld	c, 32		; 32x8=256. width of 256x192 screen
	ldir
	ld	bc, 32;64;96		; add leftover amount for wrap around.
	add	hl, bc
	dec	a
	jr	nz, disp2
	call 	delay_short





	ld	a, 30		; BANK 0. First 2k of screen.
	ld	c, 32
	out	(c), a

	ld	de, 0
	ld	bc, 0
	ld	b, 0
	ld	a,  64		; height of 256x192 2k bank 2 screen
	ld	de, $7000
disp3: 	ld	c, 32		; 32x8=256. width of 256x192 screen
	ldir
	ld	bc, 32;64;96		; add leftover amount for wrap around.
	add	hl, bc
	dec	a
	jr	nz, disp3
	call 	delay_short



	pop	hl	
	pop	bc
	pop	de
	ret






	





delay_short:
	push	bc
	push	hl
	push	de
        LD     	BC, 400		       ; delay a little
delaya:	DEC    	BC             
        LD     	A,B            
        OR     	C                   
        JR     	NZ, delaya
	pop	de
	pop	hl
	pop	bc
	ret

delay_short2:
	push	bc
	push	hl
	push	de
        LD     	BC, 200		       ; delay a little
delayab:DEC    	BC             
        LD     	A,B            
        OR     	C                   
        JR     	NZ, delayab
	pop	de
	pop	hl
	pop	bc
	ret


delay_short3:
	push	bc
	push	hl
	push	de
        LD     	BC, 1500		       ; delay a little
delayac:DEC    	BC             
        LD     	A,B            
        OR     	C                   
        JR     	NZ, delayac
	pop	de
	pop	hl
	pop	bc
	ret


delay_long:
	push	bc
	push	hl
	push	de
        LD     	BC, $ffff		       ; delay a bit more
delayb:	DEC    	BC             
        LD     	A,B            
        OR     	C                   
        JR     	NZ, delayb
	pop	de
	pop	hl
	pop	bc
	ret

counter:	dw 0,0
counter1:	db 0,0
counter2:	db 0,0
counter3:	db 0,0
offset:		dw 0,0
x:		db 0
y:		db 0
direction:	db 0





