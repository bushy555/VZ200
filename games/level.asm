screen	 equ	$7000

        org    8000h


intro:	di
	ld 	a,8			; mode (1)
	ld 	($6800),a
keyscan:ld	hl, level1		;load mario 1 and mario 2 into A000
	ld	de, scrnbuf		; 2x ((2x 16x16 images) side by side = 512 bytes) = 64x16 image, 1024 bytes.
	ld	bc, 2048
	ldir

	ld	hl, level1		; display MARIO1 on screen at $7000
	ld	de, screen
	ld	bc, 2048
	ldir

loops1b:ld 	a, ($68fd)		; PRESS <S> TO CONTINUE
	and	$02
	jr 	nz, loops1b









	ld	b, 16;64				//left	1. 
l1:	ld	a, (x)
	inc	a
	ld	(x), a
	ld	hl, level1
	ld	de, (x)
	add	hl, de
	call	display
	call 	delay_short
	djnz	l1	


	jp	sinus




display:
	push	de
	push	bc
	push	hl
	ld	de, 0
	ld	bc, 0
	ld	b,  0
	ld	a,  15;63
	ld	de, screen
disp2: 	ld	c, 32
	ldir
	ld	bc, 16;64
	add	hl, bc
	dec	a
	jr	nz, disp2
	call 	delay_short
	pop	hl	
	pop	bc
	pop	de
	ret















