	ld	ix, Sin_Table
	ld	ix, Cos_Table
	ld	de, 0

sin_1:	push	bc
	push	de

	ld	a, (ix)
	srl	a			; div SIN by 2 (0 to 64 "POKE")
	srl	a			; div SIN by 2 (0 to 32 "POKE")
;	srl	a			; div SIN by 2 (0 to 32 "POKE")

	ld	c, a			; b=0 already.

	ld	hl, mario1
;	ld	hl, mario2
	add	hl, de			; increase by 32 every step.
	add	hl, bc
	ld	de, scrnbuf
	ld	bc, 2048
	ldir	




	LD 	hl,$6800
sync0:	BIT 	7,(hl)
	jr	NZ,sync0



	ld	hl, scrnbuf
	ld	de, screen
	ld	bc, 2048
	ldir



	pop	de			; HL = DE (Y-Axis)
	ld	hl, 32;-64;32;128;64;32
	add	hl, de
	ld	d,h
	ld	e,l

	inc	ix			; increase sine table offset
	inc	ix
	pop	bc
	dec	c			; looped through 127 times?
	jr	nz, sin_1		; yes, start at 0
	jp	sinus			; no, continue looping




